function SVMOD:GetLanguage(text)
	if not text then
		return nil
	elseif self.CFG.Language == "EN" then
		return text
	elseif self.Languages[text] then
		return self.Languages[text][self.CFG.Language] or text
	else
		self:PrintConsole(SVMOD.LOG.Alert, "Unknown translation (" .. self.CFG.Language .. "): " .. text)
		return text
	end
end